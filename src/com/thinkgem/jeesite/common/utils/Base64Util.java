	public static String getImageStr(String imgFilePath) {// 将图片文件转化为字节数组字符串，并对其进行Base64编码处理
		byte[] data = null;

		// 读取图片字节数组
		try {
			InputStream in = new FileInputStream(imgFilePath);
			data = new byte[in.available()];
			in.read(data);
			in.close();
		} catch (IOException e) {
			e.printStackTrace();
		}

		// 对字节数组Base64编码
		BASE64Encoder encoder = new BASE64Encoder();
		return encoder.encode(data);// 返回Base64编码过的字节数组字符串
	}

	public static String writeImageStrToImageFile(String base64ImageArray,
			HttpServletRequest request) {// 对字节数组字符串进行Base64解码并生成图片

		if (StringUtils.isBlank(base64ImageArray)) { // 图像数据为空
			return "";
		}
		String RealPath = request.getSession().getServletContext()
				.getRealPath("");
		// F:\project_mawenli\.metadata\.plugins\org.eclipse.wst.server.core\tmp2\wtpwebapps\litu
		String imagePathName = CommonPath.getDestFolder(request) + "/"
				+ UUID.randomUUID().toString() + ".jpg";

		File dest = new File(RealPath + imagePathName
				+ UUID.randomUUID().toString());
		if (!dest.isDirectory()) {
			dest.getParentFile().mkdirs();
		}

		// if (!dest.getParentFile().isDirectory())
		// dest.getParentFile().mkdirs();
		// try {
		// if (!dest.isFile())dest.createNewFile();
		// } catch (IOException e) {
		// e.printStackTrace();
		// }

		BASE64Decoder decoder = new BASE64Decoder();
		try {
			// Base64解码
			byte[] bytes = decoder.decodeBuffer(base64ImageArray);
			for (int i = 0; i < bytes.length; ++i) {
				if (bytes[i] < 0) {// 调整异常数据
					bytes[i] += 256;
				}
			}
			// 生成jpeg图片
			OutputStream out = new FileOutputStream(RealPath + imagePathName);
			out.write(bytes);
			out.flush();
			out.close();
			return imagePathName;
		} catch (Exception e) {
			e.printStackTrace();
			return "";
		}
	}

